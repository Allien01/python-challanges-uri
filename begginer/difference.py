'''
Read four integer values named A, B, C
and D. Calculate and print the difference
of product A and B by the product of C and D.
'''

a = int(input('Informe o primeiro numero: '))
b = int(input('Informe o segundo numero: '))
c = int(input('Informe o terceiro numero: '))
d = int(input('Informe o quarto numero: '))
result = (a*b) - (c*d)
print('DIFERENCA =', result)
